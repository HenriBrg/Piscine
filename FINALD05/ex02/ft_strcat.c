char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	if (src == 0)
		return (dest);
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
